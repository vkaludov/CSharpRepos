using System;

namespace _01DeclareVariables
{
    class DeclareVariables
    {
        static void Main()
        {
            ushort value1 = 52130;
            sbyte value2 = -115;
            int value3 = 4825932;
            byte value4 = 97;
            short value5 = -10000;
            
            Console.WriteLine("{0}\n{1}\n{2}\n{3}\n{4}", value1, value2, value3, value4, value5);
        }
    }
}
